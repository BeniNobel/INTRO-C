// Program to compare two integers (a and b) and print whether they are equal.

#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main() {
    // Declare and initialize integer variables a and b.
    int a = 2;
    int b = 9;

    // Check if the values of a and b are equal and print the result.
    if (a == b)
        printf("a and b are equal.");
    else
        printf("a and b are not equal.");

    // Return 0 to indicate successful execution.
    return 0;
}
