// Program to print the values of 'i' in a loop from 0 to 9.

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main() {
    // Declare and initialize the loop variable
    int i = 0;

    // Loop from 0 to 9 and print the values of 'i'
    for (i = 0; i < 10; i++) {
        printf("\n i = %d", i);
    }

    // Return 0 to indicate successful execution
    return 0;
}
