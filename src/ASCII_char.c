// Program to increment and print the ASCII value of a character.

#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main() {
    // Declare variable 'c' and assign the character 'A'.
    char c = 'A';

    // Increment the ASCII value of 'c' by 1 and print the result.
    printf("%c", c + 1);

    // Return 0 to indicate successful execution.
    return 0;
}
